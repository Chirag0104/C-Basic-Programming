#include<iostream>
using namespace std;
int main(){
    int t,n,m,s,count;
    cin>>t;
    for(int i=0;i<t;i++){
        cin>>n>>m>>s;
        for(int i=s;i<s+m;i++){
            if(i>=n){
                i=1;
                
            }
            count=i;
            
            
        }
        cout<<count<<endl;
        
    }
}